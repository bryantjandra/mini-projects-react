//Mutating props affect the parent, creating side effects.
//React components have to be pure functions in terms of props and state.
//This is why props are immutable.

//A piece of JSX can only have 1 root element.
//In JSX, we write CSS inline styles like this: {{<style>}}, this is to reference a variable and then an object.

function Pizza({ pizzaObj }) {
  return (
    <li className={`pizza ${pizzaObj.soldOut ? "sold-out" : ""}`}>
      <img src={pizzaObj.photoName} alt={pizzaObj.name} />
      <div>
        <h3>{pizzaObj.name}</h3>
        <p>{pizzaObj.ingredients}</p>
        <span>{pizzaObj.soldOut ? "SOLD OUT" : pizzaObj.price}</span>
      </div>
    </li>
  );
}

